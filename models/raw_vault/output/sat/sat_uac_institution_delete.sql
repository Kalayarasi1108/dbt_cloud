INSERT INTO DATA_VAULT.CORE.SAT_UAC_INSTITUTION (SAT_UAC_INSTITUTION_SK, HUB_UAC_INSTITUTION_KEY, SOURCE,
                                                     LOAD_DTS, ETL_JOB_ID, HASH_MD5,IS_DELETED)

WITH SAT_UAC_INST_LATEST AS (
    SELECT HUB.HUB_UAC_INSTITUTION_KEY,
           HUB.SOURCE,
           SAT.IS_DELETED,
           LEAD(SAT.LOAD_DTS)
                OVER (PARTITION BY SAT.HUB_UAC_INSTITUTION_KEY,SAT.HASH_MD5 ORDER BY SAT.LOAD_DTS ASC) EFFECTIVE_END_DTS
    FROM DATA_VAULT.CORE.SAT_UAC_INSTITUTION SAT
             JOIN DATA_VAULT.CORE.HUB_UAC_INSTITUTION HUB
                  ON HUB.HUB_UAC_INSTITUTION_KEY = SAT.HUB_UAC_INSTITUTION_KEY
)
SELECT DATA_VAULT.CORE.SEQ.nextval               SAT_UAC_INSTITUTION_SUM_SK,
       S.HUB_UAC_INSTITUTION_KEY,
       S.SOURCE,
       CURRENT_TIMESTAMP::TIMESTAMP_NTZ          LOAD_DTS,
       'SQL' || CURRENT_TIMESTAMP::TIMESTAMP_NTZ ETL_JOB_ID,
       MD5('')                                   HASH_MD5,
       'Y'                                       IS_DELETED
FROM SAT_UAC_INST_LATEST S

WHERE S.EFFECTIVE_END_DTS IS NULL
  AND S.IS_DELETED = 'N'
  AND NOT EXISTS(
        SELECT NULL
        FROM ODS.UAC.VW_ALL_INST INST
        WHERE S.HUB_UAC_INSTITUTION_KEY = MD5(
                    IFNULL(INST.CODE, '') || ',' ||
                    IFNULL(INST.YEAR, '') || ',' ||
                    IFNULL(INST.SOURCE, '')
            )
    );