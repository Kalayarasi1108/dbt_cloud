INSERT INTO DATA_VAULT.CORE.SAT_UAC_PREFERENCE_HISTORY (SAT_UAC_PREFERENCE_HISTORY_SK, HUB_UAC_PREFERENCE_HISTORY_KEY,
                                                        SOURCE, LOAD_DTS,
                                                        ETL_JOB_ID, HASH_MD5, IS_DELETED)
SELECT DATA_VAULT.CORE.SEQ.NEXTVAL               SAT_UAC_PREFERENCE_HISTORY_SK,
       S.HUB_UAC_PREFERENCE_HISTORY_KEY,
       S.SOURCE                                  SOURCE,
       CURRENT_TIMESTAMP::TIMESTAMP_NTZ          LOAD_DTS,
       'SQL' || CURRENT_TIMESTAMP::TIMESTAMP_NTZ ETL_JOB_ID,
       MD5('')                                   HASH_MD5,
       'Y'                                       IS_DELETED
FROM (
         SELECT HUB.YEAR,
                HUB.SOURCE,
                HUB.STATDATE,
                HUB.STATGRP,
                HUB.PREFNUM,
                HUB.COURSE,
                SAT.HUB_UAC_PREFERENCE_HISTORY_KEY,
                SAT.LOAD_DTS,
                LEAD(SAT.LOAD_DTS)
                     OVER (PARTITION BY SAT.HUB_UAC_PREFERENCE_HISTORY_KEY ORDER BY SAT.LOAD_DTS ASC) EFFECTIVE_END_DTS,
                IS_DELETED
         FROM DATA_VAULT.CORE.SAT_UAC_PREFERENCE_HISTORY SAT
                  JOIN DATA_VAULT.CORE.HUB_UAC_PREFERENCE_HISTORY HUB
                       ON HUB.HUB_UAC_PREFERENCE_HISTORY_KEY = SAT.HUB_UAC_PREFERENCE_HISTORY_KEY
     ) S
WHERE S.EFFECTIVE_END_DTS IS NULL
  AND S.IS_DELETED = 'N'
  AND NOT EXISTS(
        SELECT NULL
        FROM ODS.UAC.VW_ALL_GENSTAT1 GENSTAT
        WHERE GENSTAT.YEAR = S.YEAR
          AND GENSTAT.SOURCE = S.SOURCE
          AND GENSTAT.STATDATE::DATE = S.STATDATE
          AND GENSTAT.STATGRP = S.STATGRP
          AND GENSTAT.PREFNUM = S.PREFNUM
          AND GENSTAT.COURSE = S.COURSE
    )
