INSERT INTO DATA_VAULT.CORE.SAT_COURSE_APPLICATION_SUM (SAT_COURSE_APPLICATION_SUM_SK,
                                                        HUB_COURSE_APPLICATION_KEY,
                                                        SOURCE, LOAD_DTS, ETL_JOB_ID, HASH_MD5,
                                                        IS_DELETED)

WITH SAT_COURSE_APPLICATION_SUM AS (
    SELECT HUB_COURSE_APPLICATION_KEY,
           HASH_MD5,
           LOAD_DTS,
           IS_DELETED,
           LEAD(LOAD_DTS) OVER (PARTITION BY HUB_COURSE_APPLICATION_KEY ORDER BY LOAD_DTS) EFFECTIVE_END_DTS
    FROM DATA_VAULT.CORE.SAT_COURSE_APPLICATION_SUM
),
     SAT_COURSE_APPLICATION_ODS AS (
         SELECT MD5(IFNULL(A.STU_ID, '') || ',' ||
                    IFNULL(S.SPK_NO, 0) || ',' ||
                    IFNULL(S.SPK_VER_NO, 0) || ',' ||
                    IFNULL(CONCAT(S.APPLICATION_ID, '_', S.APPLICATION_LINE_ID), '')) HUB_COURSE_APPLICATION_KEY
         FROM ODS.AMIS.S1APP_APPLICATION AS A
                  INNER JOIN ODS.AMIS.S1APP_APPLICATION_LINE AS L ON A.APPLICATION_ID = L.APPLICATION_ID
                  INNER JOIN ODS.AMIS.S1APP_STUDY AS S ON L.APPLICATION_ID = S.APPLICATION_ID
             AND S.APPLICATION_LINE_ID = L.APPLICATION_LINE_ID
                  LEFT JOIN ODS.AMIS.S1APP_OFFER AS O ON O.APPLICATION_ID = L.APPLICATION_ID
             AND L.APPLICATION_LINE_ID = O.APPLICATION_LINE_ID
         UNION ALL
         SELECT MD5(IFNULL(APP.STU_ID, '') || ',' ||
                    IFNULL(APP.SPK_NO, 0) || ',' ||
                    IFNULL(APP.SPK_VER_NO, 0) || ',' ||
                    IFNULL(APP.APPN_NO, 0)) HUB_COURSE_APPLICATION_KEY
         FROM ODS.AMIS.S1STU_APPLICATION APP
     )
SELECT DATA_VAULT.CORE.SEQ.NEXTVAL               SAT_COURSE_APPLICATION_SUM_SK,
       S.HUB_COURSE_APPLICATION_KEY,
       'AMIS'                                    SOURCE,
       CURRENT_TIMESTAMP::TIMESTAMP_NTZ AS       LOAD_DTS,
       'SQL' || CURRENT_TIMESTAMP::TIMESTAMP_NTZ ETL_JOB_ID,
       MD5('')                          AS       HASH_MD5,
       'Y'                              AS       IS_DELETED

FROM SAT_COURSE_APPLICATION_SUM S
WHERE S.EFFECTIVE_END_DTS IS NULL
  AND IS_DELETED = 'N'
  AND NOT EXISTS(
        SELECT NULL
        FROM SAT_COURSE_APPLICATION_ODS APP
        WHERE S.HUB_COURSE_APPLICATION_KEY = APP.HUB_COURSE_APPLICATION_KEY
    );